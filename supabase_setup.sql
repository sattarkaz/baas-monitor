-- ── 0. Clean slate (yenidən icra etmək üçün təhlükəsiz) ──────────────
DROP TABLE IF EXISTS partner_docs CASCADE;
DROP TABLE IF EXISTS packages     CASCADE;
DROP TABLE IF EXISTS sys_users    CASCADE;
DROP TABLE IF EXISTS partners     CASCADE;

-- ── 1. Tables ────────────────────────────────────────────────────────

CREATE TABLE partners (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name          TEXT NOT NULL,
  form          TEXT NOT NULL DEFAULT 'MMC',
  status        TEXT NOT NULL DEFAULT 'active',
  date          TEXT DEFAULT '',
  email         TEXT DEFAULT '',
  phone         TEXT DEFAULT '',
  mgr           TEXT DEFAULT '',
  voen          TEXT DEFAULT '',
  signatory     TEXT DEFAULT '',
  signatory_pos TEXT DEFAULT '',
  bank_name     TEXT DEFAULT '',
  bank_account  TEXT DEFAULT '',
  bank_code     TEXT DEFAULT '',
  legal_address TEXT DEFAULT '',
  created_at    TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE packages (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  partner_id BIGINT NOT NULL REFERENCES partners(id) ON DELETE CASCADE,
  size       INTEGER NOT NULL DEFAULT 0,
  start_date TEXT DEFAULT '',
  end_date   TEXT DEFAULT '',
  status     TEXT NOT NULL DEFAULT 'active',
  issued     INTEGER NOT NULL DEFAULT 0,
  rem        INTEGER NOT NULL DEFAULT 0,
  pct        NUMERIC(6,2) NOT NULL DEFAULT 0,
  notes      TEXT DEFAULT '',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE sys_users (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  login      TEXT UNIQUE NOT NULL,
  name       TEXT NOT NULL,
  email      TEXT DEFAULT '',
  role       TEXT NOT NULL DEFAULT 'viewer',
  status     TEXT NOT NULL DEFAULT 'active',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE partner_docs (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  partner_id BIGINT NOT NULL REFERENCES partners(id) ON DELETE CASCADE,
  name       TEXT NOT NULL,
  size       INTEGER DEFAULT 0,
  mime       TEXT DEFAULT '',
  date       TEXT DEFAULT '',
  note       TEXT DEFAULT '',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- ── 2. Row Level Security — ictimai daxili alət üçün tam icazə ───────
ALTER TABLE partners     ENABLE ROW LEVEL SECURITY;
ALTER TABLE packages     ENABLE ROW LEVEL SECURITY;
ALTER TABLE sys_users    ENABLE ROW LEVEL SECURITY;
ALTER TABLE partner_docs ENABLE ROW LEVEL SECURITY;

CREATE POLICY "allow_all" ON partners     FOR ALL TO anon USING (true) WITH CHECK (true);
CREATE POLICY "allow_all" ON packages     FOR ALL TO anon USING (true) WITH CHECK (true);
CREATE POLICY "allow_all" ON sys_users    FOR ALL TO anon USING (true) WITH CHECK (true);
CREATE POLICY "allow_all" ON partner_docs FOR ALL TO anon USING (true) WITH CHECK (true);

